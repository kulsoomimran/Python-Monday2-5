print("Assignment Operators")

x = 9
print("Initial value of x:", x)

x += 1
print("After x += 1: ", x) 

x -= 4
print("After x -= 4: ", x)

x *= 3
print("After x *= 3: ", x)

x /= 2
print("After x /= 2: ", x)

x //= 5
print("After x //= 5: ", x)

x %= 3
print("After x %= 3: ", x)

x **= 2
print("After x **= 2: ", x)